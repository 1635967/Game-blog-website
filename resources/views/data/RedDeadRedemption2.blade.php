						<h2>Description</h2>
						<p><b>Red Dead Redemption 2</b> is a 2018 action-adventure game developed and published by Rockstar Games. 
						The game is the third entry in the Red Dead series and is a prequel to the 2010 game Red Dead Redemption. 
						The story is set in 1899 in a fictionalized representation of the Western, Midwestern and Southern United 
						States and follows outlaw Arthur Morgan, a member of the Van der Linde gang. Arthur must deal with the decline of 
						the Wild West whilst attempting to survive against government forces, rival gangs, and other adversaries. 
						The story also follows fellow gang member John Marston, the protagonist of Red Dead Redemption.</p>
						
						<p>The game is presented through both first and third-person perspectives, and the player may freely roam in its 
						interactive open world. Gameplay elements include shootouts, heists, hunting, horseback riding, interacting with 
						non-player characters, and maintaining the character's honor rating through moral choices and deeds. A bounty 
						system similar to the "wanted" system from the Grand Theft Auto franchise governs the response of law enforcement 
						and bounty hunters to crimes committed by the player. Red Dead Redemption 2 was released for the PlayStation 4 
						and Xbox One in October 2018, and for Microsoft Windows and Stadia in November 2019. Red Dead Online, the online 
						multiplayer mode of the game, was released as a beta version in November 2018 before a full release in May 2019.</p>
						
						<p>The game's development began soon after Red Dead Redemption's release and was shared between all of Rockstar's 
						studios worldwide. The development team drew influence from real locations as opposed to film or art, focused on 
						creating an accurate reflection of the time with the game's characters and world. The game was Rockstar's first 
						built specifically for eighth generation consoles, having tested their technical capabilities while porting Grand 
						Theft Auto V to the platforms. The game's soundtrack features an original score composed by Woody Jackson and 
						several vocal tracks produced by Daniel Lanois.</p>
						
						<p>Broadly anticipated and marketed before release, Red Dead Redemption 2 broke several records and had the 
